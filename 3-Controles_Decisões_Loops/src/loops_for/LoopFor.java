package loops_for;

public class LoopFor {

	public static void main(String[] args) {
		System.out.println("Forma crescente:");
		for (int i = 0;i <= 5; i++) {
			System.out.println("i = " +i);
		}
		System.out.println();
		System.out.println("Forma decrescente:");
		for (int i = 5;i >= 0; i--) {
			System.out.println("i = " +i);
		}

	}

}
